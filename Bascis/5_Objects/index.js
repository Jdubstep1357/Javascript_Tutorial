// Object

// Object - A variable with subcategories (properties)

let name = "Mosh";
let age = 30;

// { } objct literal
const person = {
    // Keys are properties of objects
    name: "John",
    age: 30
};


// Dot Notation - change part of property
person.name = "Macy";


// Bracket notation
let selection = "Name";
person[selection] = "Grace";


