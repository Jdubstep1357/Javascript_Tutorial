let x = 10;
let y = 3;

// Addition Operator
console.log(x + y);
// Subtraction
console.log(x - y);
// Multiplication
console.log(x * y);
// Divisible
console.log(x / y);
// Remainder 
console.log(x % y);
/// Exponentiation - x to power of y
console.log(x ** y);

console.log("Increment and decrement operators: ");

// Increment (++)
// Makes value of x incremented by one first then on console
console.log(++x);
// Console first then x adds one
console.log(x++);


// Decrement operator
console.log(--x);
console.log(x--);
